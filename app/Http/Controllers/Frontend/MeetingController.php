    {
        $pro=new Meeting;
        $pro->course=$request->course;
        $pro->time=$request->time;
        $pro->students=$request->stu;
        $pro->teacher=Auth::user('teacher')->name;
        $pro->save(); 

        return redirect('teacherf');
    }

